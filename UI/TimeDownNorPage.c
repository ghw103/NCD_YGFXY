static MyState_TypeDef activityBufferMalloc(void)
{
	if(NULL == S_TimeDownPageData)
	{
		S_TimeDownPageData = MyMalloc(sizeof(TimeDownPageData));
		
		if(S_TimeDownPageData)
		{
			memset(S_TimeDownPageData, 0, sizeof(TimeDownPageData));
	
			return My_Pass;
		}
		else
			return My_Fail;
	}
	else
		return My_Pass;
}

