/***************************************************************************************************
*FunctionName：Led_GPIOInit
*Description：初始化系统指示灯的IO
*Input：None
*Output：None
*Author：xsx
*Data：2015年8月26日14:34:45
***************************************************************************************************/
void Led_GPIOInit(void)
{
	GPIO_InitTypeDef GPIO_InitStructure;
	RCC_AHB1PeriphClockCmd(Led_Rcc, ENABLE); /*使能LED灯使用的GPIO时钟*/

  	GPIO_InitStructure.GPIO_Pin = Led_Pin; 
  	GPIO_InitStructure.GPIO_Mode = GPIO_Mode_OUT;
	GPIO_InitStructure.GPIO_OType = GPIO_OType_PP;
	GPIO_InitStructure.GPIO_Speed = GPIO_Speed_100MHz;
	GPIO_InitStructure.GPIO_PuPd = GPIO_PuPd_UP;
	GPIO_Init(Led_GpioGroup, &GPIO_InitStructure);
  	SetLedState(ON);
}

/***************************************************************************************************
*FunctionName：SetLedState
*Description：改变LED灯的状态
*Input：LED_State --- ON	灯亮
*						OFF	灯灭
*Output：None
*Author：xsx
*Data：2015年8月26日14:42:41
***************************************************************************************************/
void SetLedState(LED_State state)
{
	GPIO_WriteBit(Led_GpioGroup, Led_Pin, (BitAction)state);
}

/***************************************************************************************************
*FunctionName：LedToggle
*Description：LED电平反转
*Input：None
*Output：None
*Author：xsx
*Data：2016年5月14日15:47:07
***************************************************************************************************/
void LedToggle(void)
{
	GPIO_ToggleBits(Led_GpioGroup, Led_Pin);
}




