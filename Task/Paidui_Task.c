void StartPaiduiTask(void)
{
	xTaskCreate( PaiduiTask, "PaiduiTask", configMINIMAL_STACK_SIZE, NULL, ( ( unsigned portBASE_TYPE ) 2U ), NULL );
}

static void PaiduiTask(void * parm)
{
	while(1)
	{
		PaiDuiHandler();
		
		vTaskDelay(500 / portTICK_RATE_MS);
	}
}

